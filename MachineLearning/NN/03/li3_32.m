 clear all;
%输入向量P，期望值为T
P=-1:.1:1;
T=[-.9602 -.5770 -.0279 .3771 .6405 .6600 .4609 .1336 -.2013 -.4344 ...
 -.5000 -.3930 -.1647 .0988 .3072 .3960 .3449 .1816 -0.0312 -.2189 -.3021];
%绘制训练样本散点图
plot(P,T,'p');                %效果如图3-102所示
xlabel('输入向量P')
ylabel('目标向量T')
%设计一个径向基网络，网络有两层，隐层为径向基神经元，输出层为线性神经元
eg=0.02;
sc=.01;
net=newrb(P,T,eg,sc);                  %效果如图3-103所示
%网络测试
X=-1:.01:1;
Y=sim(net,X);         %网络仿真
hold on;
plot(X,Y,'r');            %效果如图3-104
