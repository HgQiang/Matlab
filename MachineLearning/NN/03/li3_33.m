 clear all;
% 输入向量为P，期望值为T
P=-1:.1:1;
T=[-.9602 -.5770 -.0279 .3771 .6405 .6600 .4609 .1336 -.2013 -.4344 ...
-.5000 -.3930 -.1647 .0988 .3072 .3960 .3449 .1816 -0.0312 -.2189 -.3021];
%绘制训练样本散点图
plot(P,T,'rp');             %效果如图3-105所示
xlabel('输入向量P')
ylabel('目标向量T')
%快速构建一个径向基函数网络，并且网络根据输入向量和期望自动进行调整，
%从而进行函数逼近
eg=0.02;
sc=100;
net=newrb(P,T,eg,sc);                  %效果如图3-106
%网络测试
Y=sim(net,P);        %网络仿真
hold on;
plot(P,Y);            %效果如图3-107
hold off;
